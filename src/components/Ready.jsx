import React, { useState } from "react";
import ReadyImg from "../assets/images/Ready.png";
import toast from "react-hot-toast";
import { useNavigate } from "react-router-dom";

const Ready = () => {
  const navigate = useNavigate();
  const [email, setEmail] = useState("");
  const [loading, setLoading] = useState(false);

  // simple email validation
  const isValidEmail = (email) =>
    /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email);

  const handleSubscribe = (e) => {
    e.preventDefault();

    if (!email) {
      toast.error("Email is required");
      return;
    }

    if (!isValidEmail(email)) {
      toast.error("Please enter a valid email address");
      return;
    }

    setLoading(true);

    // Mailchimp embedded form submission
    const mailchimpUrl =
      "https://iyiolafunke1@gmail.com/subscribe/post-json?u=YOUR_U_ID&id=YOUR_LIST_ID&c=?&EMAIL=" +
      encodeURIComponent(email);

    const script = document.createElement("script");
    script.src = mailchimpUrl;
    script.onload = () => setLoading(false);
    document.body.appendChild(script);

    toast.success("Subscribed successfully üéâ");
    setEmail("");
  };

  const handleGetStarted = () => {
    navigate("/signup");
  };

  return (
    <section className="py-2 px-4 sm:px-8 lg:px-16">
      <div className="relative mx-auto rounded-3xl overflow-hidden shadow-lg">
        {/* IMAGE */}
        <img
          src={ReadyImg}
          alt="Ready to start your tech career"
          className="w-full h-[530px] object-cover"
        />

        {/* OVERLAY */}
        <div className="absolute inset-0 bg-gradient-to-t from-black/70 via-black/40 to-transparent" />

        {/* CONTENT */}
        <div className="absolute inset-0 flex flex-col justify-end px-6 sm:px-10 pb-8 lg:ml-[550px]">
          <h1 className="text-2xl sm:text-3xl lg:text-4xl font-bold text-white mb-3">
            Exam Info
          </h1>

          <p className="text-sm sm:text-base text-gray-200 mb-5 max-w-xl">
            At the end of your accelerated master class course, you are guaranteed
            your certification pass at one sitting:
          </p>

          <p className="text-sm sm:text-base text-gray-200 mb-5 whitespace-pre-line max-w-md">
            üìù Exam Name: CSM{"\n"}
            ‚è± Duration: 60 mins{"\n"}
            üìÑ Format: Multiple-choice{"\n"}
            ‚ùì Number of questions: 50{"\n"}
            ‚úÖ Passing score: 60%
          </p>

          {/* GET STARTED */}
          <button
            onClick={handleGetStarted}
            className="mb-6 px-8 py-3 rounded-xl bg-white text-[#15256E] font-medium hover:bg-gray-100 w-full sm:w-40 text-start"
          >
            Get Started
          </button>

          {/* NEWSLETTER */}
          <div className="w-full max-w-md bg-white/95 rounded-2xl p-4">
            <p className="text-sm font-medium text-gray-900 mb-3">
              Subscribe to our newsletter
            </p>

            <form onSubmit={handleSubscribe} className="flex flex-col sm:flex-row gap-3">
              <input
                type="email"
                placeholder="Enter your email address"
                value={email}
                onChange={(e) => setEmail(e.target.value)}
                className="w-full px-4 py-3 rounded-xl border border-gray-300 text-sm focus:outline-none focus:ring-2 focus:ring-[#001489]"
              />

              <button
                type="submit"
                disabled={loading || !email}
                className={`px-6 py-3 rounded-xl text-white text-sm font-medium transition
                  ${loading || !email ? "bg-gray-400 cursor-not-allowed" : "bg-gray-900 hover:bg-black"}`}
              >
                {loading ? "Sending..." : "Subscribe"}
              </button>
            </form>
          </div>
        </div>
      </div>
    </section>
  );
};

